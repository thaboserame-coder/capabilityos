export function getBUById(id) {
  return BUSINESS_UNITS.find((b) => b.id === id);
}

export function getLearnersForBU(buId) {
  return DEMO_LEARNERS.filter((l) => l.bu === buId);
}

export function getInterventionsForBU(buId) {
  return INTERVENTIONS.filter((i) => i.bu === buId || i.bu === "all");
}

export function getRiskColor(score) {
  if (score >= 80) return "#D64545";
  if (score >= 60) return "#E8743B";
  if (score >= 40) return "#D4A72C";
  return "#1F9D55";
}

export function getRiskLabel(score) {
  if (score >= 80) return "Critical";
  if (score >= 60) return "High";
  if (score >= 40) return "Medium";
  return "Low";
}

export function getStatusColor(status) {
  switch (status) {
    case "active": return "#1F9D55";
    case "at-risk": return "#E8743B";
    case "inactive": return "#8C9AB3";
    default: return "#8C9AB3";
  }
}
